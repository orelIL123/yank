const normalizeLocale = (value) => {
  if (!value) return 'he'
  const locale = String(value).toLowerCase()
  if (locale.startsWith('fr')) return 'fr'
  if (locale.startsWith('en')) return 'en'
  if (locale.startsWith('ru')) return 'ru'
  return 'he'
}

let currentLocale = 'he'

export const initLocale = async () => {
  try {
    const saved = await AsyncStorage.getItem(LANGUAGE_STORAGE_KEY)
    currentLocale = normalizeLocale(saved || 'he')
  } catch (error) {
    currentLocale = 'he'
  }
  return currentLocale
}

export const getLocale = () => currentLocale

export const setLocale = async (locale) => {
  const normalized = normalizeLocale(locale)
  currentLocale = normalized
  try {
    await AsyncStorage.setItem(LANGUAGE_STORAGE_KEY, normalized)
  } catch (error) {
    // Keep in-memory locale if storage fails
  }
  return normalized
}

export const t = (key) => {
  const locale = currentLocale || 'he'
  return translations[locale]?.[key] || translations.he?.[key] || key
}

/** Translation by explicit language (e.g. prayer screen language selector). lang: 'he'|'en'|'ru'|'fr' */
export const tByLang = (lang, key) => {
  const l = lang && translations[lang] ? lang : 'he'
  return translations[l]?.[key] || translations.he?.[key] || key
}

export const getAllTranslations = (key) => {
  return {
    he: translations.he[key] || key,
    fr: translations.fr[key] || key,
    en: translations.en[key] || key,
  }
}

